// Get language from env (default to English)
const getLanguage = () => {
  if (typeof process !== 'undefined' && process.env) {
    const lang = process.env.NEXT_PUBLIC_LANGUAGE;

    console.log("ENV LANG:", lang);

    if (lang === '2') return 'hi';
  }

  return 'en';
};
// Get translated text
export const t = (key) => {
  const lang = getLanguage();
  console.log(lang);
  return translations[lang][key] || translations['en'][key] || key;
};

// Get current language
export const getCurrentLanguage = () => getLanguage();

export default translations;
